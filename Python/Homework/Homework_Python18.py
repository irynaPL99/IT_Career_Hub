'''Task 1. Не уникальные числа
Напишите программу, которая находит все числа,
встречающиеся !более одного раза! в списке, и выводит их в порядке убывания.
Данные:
numbers = [4, 7, 3, 7, 8, 3, 4, 2, 7, 3, 8, 4]
Пример вывода:
Числа, встречающиеся более одного раза: [7, 4, 3, 8]
'''

print("\n<<<<< Task 1.1 Не уникальные числа  >>>>>")
numbers = [4, 7, 3, 7, 8, 3, 4, 2, 7, 3, 8, 4]
output_numbers = []
for item in numbers:
    if item not in output_numbers and  numbers.count(item) > 1:
        output_numbers.append(item)
output_numbers.sort(reverse=True)
#print(f"Числа, встречающиеся более одного раза: {output_numbers}")
print(f"Числа, встречающиеся более одного раза (в порядке убывания):"
      f" {output_numbers}")

print("\n<<<<< Task 1.2 Не уникальные числа  >>>>>")
numbers = [4, 7, 3, 7, 8, 3, 4, 2, 7, 3, 8, 4]
output_numbers = sorted(set([item  for item in numbers if numbers.count(item) > 1]), reverse=True)
#print(f"Числа, встречающиеся более одного раза: {output_numbers}")
print(f"Числа, встречающиеся более одного раза (в порядке убывания):"
      f" {output_numbers}")

