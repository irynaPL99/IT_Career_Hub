'''Task 3(added). Из чисел в слова
Напишите программу, которая заменяет числовые значения в словаре 
на их строковое представление (например, 1 → "один").
Используйте заранее подготовленный словарь чисел.
Данные:
# Словарь сопоставлений
number_to_word = {1: "один", 2: "два", 3: "три"}
# Исходные данные
data = {"x": 1, "y": 2, "z": 3}
Пример вывода:
{'x': 'один', 'y': 'два', 'z': 'три'}
'''
print("\n<<<<< Task 3.1 Из чисел в слова  >>>>>")
number_to_word = {1: "один", 2: "два", 3: "три"}
data = {"x": 1, "y": 2, "z": 3}
for key in data:
    key_number = data[key]
    data.update({key:number_to_word[key_number]})
print(data)